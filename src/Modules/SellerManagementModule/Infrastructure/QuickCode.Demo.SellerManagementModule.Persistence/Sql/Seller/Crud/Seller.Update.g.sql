UPDATE [dbo].[SELLERS]
SET
    [USER_ID] = @PRM_SELLER_USER_ID,
    [COMPANY_NAME] = @PRM_SELLER_COMPANY_NAME,
    [CONTACT_EMAIL] = @PRM_SELLER_CONTACT_EMAIL,
    [CONTACT_PHONE] = @PRM_SELLER_CONTACT_PHONE,
    [STATUS] = @PRM_SELLER_STATUS,
    [SELLER_TIER_ID] = @PRM_SELLER_SELLER_TIER_ID,
    [JOINED_DATE] = @PRM_SELLER_JOINED_DATE
WHERE
    [ID] = @PRM_SELLER_ID
    AND [IsDeleted] = 0;