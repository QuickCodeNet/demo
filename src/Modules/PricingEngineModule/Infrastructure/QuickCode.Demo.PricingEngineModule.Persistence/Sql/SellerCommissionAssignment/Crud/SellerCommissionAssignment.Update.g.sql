UPDATE [dbo].[SELLER_COMMISSION_ASSIGNMENTS]
SET
    [COMMISSION_MODEL_ID] = @PRM_SELLER_COMMISSION_ASSIGNMENT_COMMISSION_MODEL_ID,
    [EFFECTIVE_DATE] = @PRM_SELLER_COMMISSION_ASSIGNMENT_EFFECTIVE_DATE
WHERE
    [SELLER_ID] = @PRM_SELLER_COMMISSION_ASSIGNMENT_SELLER_ID
    AND EXISTS (
    SELECT 1
    FROM [dbo].[COMMISSION_MODELS] qc_sd_p
    WHERE qc_sd_p.[ID] = [dbo].[SELLER_COMMISSION_ASSIGNMENTS].[COMMISSION_MODEL_ID]
      AND qc_sd_p.[IsDeleted] = 0);