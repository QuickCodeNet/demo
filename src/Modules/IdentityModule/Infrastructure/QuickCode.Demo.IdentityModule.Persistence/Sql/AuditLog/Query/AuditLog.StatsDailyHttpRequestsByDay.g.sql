SELECT FORMAT([TIMESTAMP], 'yyyy-MM-dd') AS REQUEST_DAY,
	COUNT(*) AS TOTAL_REQUESTS,
	AVG(AL.[DURATION_MS]) AS AVG_DURATION_MS,
	MAX(AL.[DURATION_MS]) AS MAX_DURATION_MS,
	MIN(AL.[DURATION_MS]) AS MIN_DURATION_MS 
FROM [AUDIT_LOGS] AL 
WHERE AL.[ENTITY_NAME] = 'HttpRequest' 
	AND AL.[TIMESTAMP] > DATEADD(DAY, -6,
	GETDATE()) 
GROUP BY FORMAT([TIMESTAMP], 'yyyy-MM-dd') 
ORDER BY FORMAT([TIMESTAMP], 'yyyy-MM-dd') 