SELECT AL.[ID], AL.[TIMESTAMP], AL.[DURATION_MS], AL.[STATUS_CODE], AL.[REQUEST_MODULE], AL.[ACTION], AL.[USER_GROUP], AL.[IS_SUCCESS], AL.[ERROR_MESSAGE], AL.[NEW_VALUES] 
FROM [AUDIT_LOGS] AL 
WHERE AL.[ENTITY_NAME] = 'HttpRequest' 
	AND AL.[REQUEST_MODULE] = @PRM_AUDIT_LOG_REQUEST_MODULE 
ORDER BY AL.[TIMESTAMP] DESC 
OFFSET @StartIndex ROWS FETCH NEXT @PageSize ROWS ONLY 