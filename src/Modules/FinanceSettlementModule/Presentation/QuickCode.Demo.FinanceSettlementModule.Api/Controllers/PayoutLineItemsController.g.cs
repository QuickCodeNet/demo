namespace QuickCode.Demo.FinanceSettlementModule.Api.Controllers
{
    public partial class PayoutLineItemsController : QuickCodeBaseApiController
    {
        private readonly IPayoutLineItemService service;
        private readonly ILogger<PayoutLineItemsController> logger;
        private readonly IServiceProvider serviceProvider;
